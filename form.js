function showChooseMealForm() {
    var chooseMealForm = document.getElementById("chooseMealForm");
    var customizeMealForm = document.getElementById("customizeMealForm");
    chooseMealForm.style.display = "block";
    customizeMealForm.style.display = "none";
}

function showCustomizeMealForm() {
    var chooseMealForm = document.getElementById("chooseMealForm");
    var customizeMealForm = document.getElementById("customizeMealForm");
    chooseMealForm.style.display = "none";
    customizeMealForm.style.display = "block";
}
